<template>
    <div class="row">
        <form>
            <div class="col-sm-8 col-sm-offset-2 col-xs-12 col-md-6 col-md-offset-3">
                <label>Quote</label>
                <textarea class="form-control" v-model="quote"></textarea>
            </div>
            <div class="col-sm-8 col-sm-offset-2 col-xs-12 col-md-6 col-md-offset-3">
                <button class="btn btn-primary" @click.prevent="createNew">Adicionar Quote</button>
            </div>
        </form>
    </div>
</template>

<script>
export default {
    data: function () {
        return {
            quote: ''
        }
    },
    
    methods: {
        createNew: function () {
            if (this.quote.trim() == '')
                return;
            this.$emit('newQuote', this.quote)
            this.quote = ''
        }
    }
}
</script>

