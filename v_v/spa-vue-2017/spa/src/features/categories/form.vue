<script>
// import http from '@/services/http'
import { required } from 'vuelidate/lib/validators'
import { mapActions } from 'vuex'

export default {
  name: 'Form',
  data () {
    return {
      category: {
        id: 0,
        name: ''
      }
    }
  },
  validations: {
    category: {
      name: {
        required
      }
    }
  },
  watch: {
    '$route' () {
      if (this.$route.params.id != null) {
        this.fetch()
      }
    }
  },
  mounted () {
    this.fetch()
  },
  methods: {
    ...mapActions('categories', ['fetchOne', 'save']),
    async fetch () {
      const { id } = this.$route.params

      if (id !== undefined) {
        this.category = await this.fetchOne({ id })
      }
    },
    async submit () {
      if (this.isWholeModelInvalid) {
        return
      }

      const verb = this.isNew ? 'post' : 'put'
      const { category } = this

      this.save({ category, verb })
        .then(() => {
          this.$bus.$emit('display-alert', {
            type: 'success',
            message: 'Categoria salva com sucesso'
          })
        })
    }
  },
  computed: {
    isWholeModelInvalid () {
      return this.$v.$invalid
    },
    isNameInvalid () {
      return this.$v.category.name.$invalid
    },
    isNew () {
      return this.category.id === 0
    }
  }
}
</script>

<template>
  <div>
    <form @submit.prevent="submit" class="well">
      <div class="form-group"
        :class="{ 'has-error': isNameInvalid }">
        <label for="" class="control-label">Nome</label>
        <input type="text" class="form-control" v-model="category.name">
      </div>
      <div class="text-right">
        <button
        :disabled="isWholeModelInvalid"
        class="btn btn-primary btn-xs"
        type="submit">Salvar</button>
      </div>
    </form>
  </div>
</template>
